				} else {
					$body = $emailoptions["highbid"];
					$body = str_replace("\r\n", "\n", $body);
				}
				if (strlen($staticpage) > 0) {
					$link  = $staticpage."?auction_id=".$auction_id;
				} else {
					$link  = $landingpage."?auction_to_show=".$auction_id;
