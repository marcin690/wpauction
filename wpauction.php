	} // else {   echo "<!-- Going with $auction_id -->"; }

	// if we *still* don't have an ID .. let's just pick a random one
	if(!is_numeric($auction_id)) {
		$cond = "'".current_time( 'mysql' )."' < date_end order by rand() limit 1";
