// Simple engine with no proxy bidding
function wpa_process_bid_simple_engine( $auction_id, $bidder_name, $bidder_email, $bidder_url, $max_bid, $BIN_amount ) {

	global $wpdb;
	global $BID_WIN, $BID_LOSE, $BIN_WIN, $BID_WIN_REVERSE;

