	// scheduled event to ensure auctions close properly
	global $wpdb;
	$table_name = $wpdb->prefix . "wpa_auctions";
	$strSQL = "SELECT id FROM $table_name WHERE winner IS NULL";
	$rows = $wpdb->get_results ($strSQL);
	foreach ($rows as $row) {
